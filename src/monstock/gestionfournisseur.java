     public void affiche(){
         String req_affiche="select * from fournisseur";
         try{
              stm=maconx.obtenircnx().createStatement();
            ResultSet res=stm.executeQuery(req_affiche);
            while(res.next()){
                Vector v=new Vector();
                for(int i=0;i<model.getColumnCount();i++){
                     v.add(res.getInt("id"));
                    v.add(res.getString("nom_soc"));
                    v.add(res.getString("matricule"));
                    v.add(res.getString("adress"));
                    v.add(res.getString("tel"));
                    
                }
                 model.addRow(v);
            }
         }catch(SQLException ex){
             JOptionPane.showMessageDialog(this, ex.getMessage());
         }
     }
      
         
     
