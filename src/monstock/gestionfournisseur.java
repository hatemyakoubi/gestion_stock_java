    private void supprimerfourActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerfourActionPerformed

        if(tab_fournisseur.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(this,"Voulez vous selectionnée un article pour supprimer");
            return;
        }
        else{
            String requete="DELETE FROM fournisseur where id='"+Integer.parseInt(id.getText())+"'";
            try{

                stm=maconx.obtenircnx().createStatement();

                if  (JOptionPane.showConfirmDialog(this,"voulez-vous supprimer", "Verification!!!!", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION)
                {
                    stm.executeUpdate(requete);
                    model.removeRow(selectedrow);

                }
            }catch (SQLException ex) {
                JOptionPane.showConfirmDialog(this, ex.getMessage());
            }

        }
        id.setText("");
        nom.setText("");
        mf.setText("");
        adress.setText("");

        tel.setText("");
    }//GEN-LAST:event_supprimerfourActionPerformed

    private void modifierfourActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierfourActionPerformed

        if(tab_fournisseur.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(this,"Voulez vous selectionnée une ligne pour modifier");
            return;
        }
        else{
            String req_update_user="UPDATE fournisseur SET nom_soc='"+nom.getText()+"', matricule='"+mf.getText()+"', adress='"+adress.getText()+"', tel='"+tel.getText()+"' where nom_soc='"+Integer.parseInt(id.getText())+"'  ";
            try{
                if  (JOptionPane.showConfirmDialog(this,"voulez-vous modifier", "Verification!!!!", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION)
                {
                    stm=maconx.obtenircnx().createStatement();
                    stm.executeUpdate(req_update_user);

                    model.setValueAt(id.getText(), tab_fournisseur.getSelectedRow(),0);
                    model.setValueAt(nom.getText(), tab_fournisseur.getSelectedRow(),1);
                    model.setValueAt(mf.getText(), tab_fournisseur.getSelectedRow(),2);
                    model.setValueAt(adress.getText(), tab_fournisseur.getSelectedRow(),3);
                    model.setValueAt(tel.getText(), tab_fournisseur.getSelectedRow(),4);
                    id.setText("");
                    nom.setText("");
                    mf.setText("");
                    adress.setText("");
                    tel.setText("");
                }
            }catch (SQLException ex){
                System.out.println(ex);
            }

        }
    }//GEN-LAST:event_modifierfourActionPerformed

    private void ajouterfourActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouterfourActionPerformed

        if((nom.getText().equals(""))||(mf.getText().equals(""))||(adress.getText().equals(""))||(tel.getText().equals(""))){
            JOptionPane.showMessageDialog(this, "Champs vide!");
        }
        else{
            String req_user="INSERT INTO fournisseur (nom_soc, matricule, adress, tel) VALUES ('"+nom.getText()+"','"+mf.getText()+"', '"+adress.getText()+"','"+tel.getText()+"')";
            String req_sel_user="SELECT * FROM fournisseur WHERE nom_soc='"+nom.getText()+"' AND matricule='"+mf.getText()+"'";

            try{
                stm1=maconx.obtenircnx().createStatement();
                stm=maconx.obtenircnx().createStatement();
                ResultSet res=stm.executeQuery(req_sel_user);

                if(res.next()){
                    JOptionPane.showMessageDialog(this, "Ce nom est deja existe choisir un notre!!!");
                    nom.setText("");
                    mf.setText("");
                    adress.setText("");

                    tel.setText("");

                }

                else{
                    stm1.executeUpdate(req_user);
                    JOptionPane.showMessageDialog(this, "Enregistrement reussite");
                    // model.addRow(new Object[] {nom.getText(), mf.getText(), adress.getText(), tel.getText()});
                    while (model.getRowCount()>0)
                    {
                        for(int i=0;i<model.getRowCount();i++)
                        {
                            model.removeRow(0);
                        }
                    }
                    affiche();

                    nom.setText("");
                    mf.setText("");
                    adress.setText("");
                    tel.setText("");
                }

            }
            catch(SQLException ex){
                JOptionPane.showMessageDialog(null,ex.getMessage());
                return;
            }
        }
    }//GEN-LAST:event_ajouterfourActionPerformed

