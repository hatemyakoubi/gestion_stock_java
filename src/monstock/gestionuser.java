    private void supprimeruserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimeruserActionPerformed
       
        if(tab_user.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(this,"Voulez vous selectionnée un article pour supprimer");
            return;
        }
        else{ 
              String requete="DELETE FROM user where id='"+Integer.parseInt(id.getText())+"'";
              try{
        
         stm=maconx.obtenircnx().createStatement();
            
            if  (JOptionPane.showConfirmDialog(this,"voulez-vous supprimer", "Verification!!!!", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION)
      {
            stm.executeUpdate(requete);
            model.removeRow(selectedrow);
           
      }
        }catch (SQLException ex) {
            System.out.println(ex);
        }
              
        }
                id.setText("");
                nom.setText("");
                prenom.setText("");
                password.setText("");
                tel.setText("");
                
        
    }//GEN-LAST:event_supprimeruserActionPerformed

    private void modifieruserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifieruserActionPerformed
        if(tab_user.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(this,"Voulez vous selectionnée une ligne pour modifier");
            return;
        }
        else{ 
    String req_update_user="UPDATE user SET nom='"+nom.getText()+"', mot_pass='"+password.getText()+"', prenom='"+prenom.getText()+"', tel='"+tel.getText()+"', poste='"+poste.getSelectedItem()+"' where id='"+Integer.parseInt(id.getText())+"'  ";
            try{
                if  (JOptionPane.showConfirmDialog(this,"voulez-vous modifier", "Verification!!!!", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION)
                 {
             stm=maconx.obtenircnx().createStatement();
                 stm.executeUpdate(req_update_user);
                 
                  model.setValueAt(id.getText(), tab_user.getSelectedRow(),0);
                  model.setValueAt(nom.getText(), tab_user.getSelectedRow(),1);
                 model.setValueAt(prenom.getText(), tab_user.getSelectedRow(),2);
                 model.setValueAt(password.getText(), tab_user.getSelectedRow(),3);
                 model.setValueAt(tel.getText(), tab_user.getSelectedRow(),4);
                 model.setValueAt(poste.getSelectedItem(), tab_user.getSelectedRow(),5);
                 
                 id.setText("");
                  nom.setText("");
                prenom.setText("");
                password.setText("");
                tel.setText("");
               
                 } 
            }catch (SQLException ex){
            System.out.println(ex);
                 }
               
            
        }
                
        
    }//GEN-LAST:event_modifieruserActionPerformed

