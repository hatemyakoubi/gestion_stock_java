     public void affiche(){
         
         String req_affiche="select * from user";
         try{
              stm=maconx.obtenircnx().createStatement();
            ResultSet res=stm.executeQuery(req_affiche);
            while(res.next()){
                Vector v=new Vector();
                for(int i=0;i<model.getColumnCount();i++){
                    v.add(res.getInt("id"));
                    v.add(res.getString("nom"));
                    v.add(res.getString("prenom"));
                    v.add(res.getString("mot_pass"));
                   v.add(res.getString("tel"));
                    v.add(res.getString("poste"));
                    
                }
                 model.addRow(v);
            }
         }catch(SQLException ex){
             JOptionPane.showMessageDialog(this, ex.getMessage());
         }
         tab_user.setModel(model);
         }
     

