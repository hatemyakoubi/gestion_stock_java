    private void ajouterdepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouterdepActionPerformed
  
        SimpleDateFormat sdf=new SimpleDateFormat("yyyy/MM/dd");
        String date=sdf.format(date_dep.getSelectedDate().getTime());
         chid.setText("");
        if((des.getText().equals(""))||(sec.getText().equals(""))||(mon.getText().equals(""))){
            JOptionPane.showMessageDialog(this, "Champs vide!");
            return;
        }
        else{
           
            String req_dep="INSERT INTO depense (chauffeur, secteur, date,description, montant) VALUES ('"+com.getSelectedItem()+"','"+sec.getText()+"','"+date+"' , '"+des.getText()+"', '"+Integer.parseInt(mon.getText())+"')";
            try{

                stm=maconx.obtenircnx().createStatement();
                stm.executeUpdate(req_dep);
                JOptionPane.showMessageDialog(this, "Enregistrement reussite");
                model.addRow(new Object[] {com.getSelectedItem(), sec.getText(), des.getText(),date, mon.getText()});
                des.setText("");
                sec.setText("");

                mon.setText("");
            }

            catch(SQLException ex){
                JOptionPane.showMessageDialog(null,ex.getMessage());
                return;
            }
             com.setSelectedItem("--");
            sec.setText("");
            des.setText("");
            mon.setText("");
        }
        int s=0;
        for(int i=0;i<tab_dep.getRowCount();i++){
            s+= (Integer.parseInt(tab_dep.getValueAt(i,4).toString()));
            res.setText("Total :"+String.valueOf(s));
        }
    }//GEN-LAST:event_ajouterdepActionPerformed

