    private void modifierdepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierdepActionPerformed
          
        SimpleDateFormat sdf=new SimpleDateFormat("yyyy/MM/dd");
        String date=sdf.format(date_dep.getSelectedDate().getTime());
        
        if(tab_dep.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(this,"Voulez vous selectionnée une ligne pour modifier");
            return;
        }
        else{
            String req_update_dep="UPDATE depense SET chauffeur='"+com.getSelectedItem()+"', secteur='"+sec.getText()+"', date='"+date+"' , description='"+des.getText()+"', montant='"+mon.getText()+"' where id='"+Integer.parseInt(chid.getText())+"' ";
            try{
                if  (JOptionPane.showConfirmDialog(this,"voulez-vous modifier", "Verification!!!!", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION)
                {
                    stm=maconx.obtenircnx().createStatement();
                    stm.executeUpdate(req_update_dep);
                   
                   
                }
                    model.setValueAt(com.getSelectedItem(), tab_dep.getSelectedRow(),0);
                    model.setValueAt(sec.getText(), tab_dep.getSelectedRow(),1);   
                    model.setValueAt(des.getText(), tab_dep.getSelectedRow(),2);
                    model.setValueAt(date, tab_dep.getSelectedRow(),3);
                    model.setValueAt(mon.getText(), tab_dep.getSelectedRow(),4);
                     
            }catch (SQLException ex){
             
                JOptionPane.showMessageDialog(this, ex);
                return;
            }
             com.setSelectedItem("--");
            sec.setText("");
            des.setText("");
            mon.setText("");
            
        }
         int s=0;
        for(int i=0;i<tab_dep.getRowCount();i++){
            s+= (Integer.parseInt(tab_dep.getValueAt(i,4).toString()));
            res.setText("Total :"+String.valueOf(s));
        }

    }//GEN-LAST:event_modifierdepActionPerformed

