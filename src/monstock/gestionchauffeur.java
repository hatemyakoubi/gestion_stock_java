    private void ajouteruserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouteruserActionPerformed

        if((nom.getText().equals(""))||(prenom.getText().equals(""))||(tel.getText().equals(""))){
            JOptionPane.showMessageDialog(this, "Champs vide!");
        }
        else{
            String req_user="INSERT INTO chauffeur (nom, prenom, tel) VALUES ('"+nom.getText()+"','"+prenom.getText()+"','"+Integer.parseInt(tel.getText())+"')";
         try{
               
                    stm=maconx.obtenircnx().createStatement();
                        stm.executeUpdate(req_user);
                        JOptionPane.showMessageDialog(this, "Enregistrement reussite");
                        model.addRow(new Object[] {nom.getText(), prenom.getText(), tel.getText()});
                        nom.setText("");
                        prenom.setText("");
                        
                        tel.setText("");
                    }
                  
            catch(SQLException ex){
                JOptionPane.showMessageDialog(null,ex.getMessage());
                return;
            }
        }
    }//GEN-LAST:event_ajouteruserActionPerformed

    private void modifieruserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifieruserActionPerformed
        if(tab_ch.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(this,"Voulez vous selectionnée une ligne pour modifier");
            return;
        }
        else{
            String req_update_user="UPDATE chauffeur SET nom='"+nom.getText()+"', prenom='"+prenom.getText()+"', tel='"+Integer.parseInt(tel.getText())+"' where nom='"+nom.getText()+"' and prenom='"+prenom.getText()+"' ";
            try{
                if  (JOptionPane.showConfirmDialog(this,"voulez-vous modifier", "Verification!!!!", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION)
                {
                    stm=maconx.obtenircnx().createStatement();
                    stm.executeUpdate(req_update_user);
                    model.setValueAt(nom.getText(), tab_ch.getSelectedRow(),0);
                    model.setValueAt(prenom.getText(), tab_ch.getSelectedRow(),1);   
                    model.setValueAt(tel.getText(), tab_ch.getSelectedRow(),2);
                    nom.setText("");
                    prenom.setText("");
                    tel.setText("");
                }
            }catch (SQLException ex){
             
                JOptionPane.showMessageDialog(this, ex);
                return;
            }

        }

    }//GEN-LAST:event_modifieruserActionPerformed

    private void supprimeruserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimeruserActionPerformed

        if(tab_ch.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(this,"Voulez vous selectionnée un article pour supprimer");
            return;
        }
        else{
            String requete="DELETE FROM chauffeur where nom='"+nom.getText()+"' and prenom='"+prenom.getText()+"'";
            try{

                stm=maconx.obtenircnx().createStatement();

                if  (JOptionPane.showConfirmDialog(this,"voulez-vous supprimer", "Verification!!!!", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION)
                {
                    stm.executeUpdate(requete);
                    model.removeRow(selectedrow);

                }
            }catch (SQLException ex) {
                System.out.println(ex);
            }

        }
        nom.setText("");
        prenom.setText("");
        tel.setText("");

    }//GEN-LAST:event_supprimeruserActionPerformed

