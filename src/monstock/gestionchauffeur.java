     public void affiche(){
         String req_affiche="select * from chauffeur";
         try{
              stm=maconx.obtenircnx().createStatement();
            ResultSet res=stm.executeQuery(req_affiche);
            while(res.next()){
                Vector v=new Vector();
                for(int i=0;i<model.getColumnCount();i++){
                    v.add(res.getString("nom"));
                    v.add(res.getString("prenom"));
                    
                    v.add(res.getString("tel"));
                    
                }
                 model.addRow(v);
            }
         }catch(SQLException ex){
             JOptionPane.showMessageDialog(this, ex.getMessage());
         }
         tab_ch.setModel(model);
         }
     
