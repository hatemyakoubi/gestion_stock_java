    private void modofoerstockActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modofoerstockActionPerformed
        
        
     if(tab_stock.getSelectedRow()==-1){
            JOptionPane.showMessageDialog(this,"Selectionnée une ligne pour le modifier!!!!");
        }else{
         String req_update_stock="UPDATE stock SET code_art='"+code.getText()+"', desg_art='"+des.getText()+"', qte='"+Integer.parseInt(qte.getText())+"', prix_vente='"+Double.parseDouble(prix.getText())+"' WHERE id_stock='"+Integer.parseInt(id.getText())+"' ";
      
     try{
          if  (JOptionPane.showConfirmDialog(this,"voulez-vous modifier", "Verification!!!!", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION)
            {
         stm=maconx.obtenircnx().createStatement();
         stm.executeUpdate(req_update_stock);
       //JOptionPane.showMessageDialog(this,"Modification réusite");
       id.setText("");
       des.setText("");
       code.setText("");
       prix.setText("");
       qte.setText("");
     }
     }
     catch(SQLException ex){
         JOptionPane.showMessageDialog(this,ex.getMessage());
         return;
     }
      }
                while (model.getRowCount()>0)
                    
                {
                    for(int i=0;i<model.getRowCount();i++)
                    {
                        model.removeRow(0);
                    }
                }
          affiche_stock();
    }//GEN-LAST:event_modofoerstockActionPerformed

