    private void chercherarticleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chercherarticleActionPerformed
               Boolean res=false;
        if (cher.getText().trim().equals("")){
                   JOptionPane.showMessageDialog(this, "Rien à chercher!!");
                   
               }else{
                  
         for(int i=0;i<tab_stock.getRowCount();i++)
        {
            if((tab_stock.getValueAt(i,1).equals(cher.getText()))||(tab_stock.getValueAt(i,2).equals(cher.getText())))
            {
                tab_stock.setRowSelectionInterval(i, i);
                res=true;
            }
          
        }
               
          }
        if(res==false){
              JOptionPane.showMessageDialog(this, "article introuvable ..");
              cher.setText("");
        }
    }//GEN-LAST:event_chercherarticleActionPerformed

