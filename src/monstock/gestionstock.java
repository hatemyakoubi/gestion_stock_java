    public void affiche_stock(){
   
       String requete_stock="SELECT * FROM stock";
        try{
            stm=maconx.obtenircnx().createStatement();
            ResultSet res=stm.executeQuery(requete_stock);
            
            
            while(res.next()){
                Vector v=new Vector();
                
                for(int i=0;i<model.getColumnCount();i++){
                    v.add(res.getInt("id_stock"));
                    v.add(res.getString("code_art"));
                     v.add(res.getString("desg_art"));
                      v.add(res.getInt("qte"));
                       v.add(res.getString("prix_vente"));
                       v.add(res.getInt("qte")*res.getInt("prix_vente"));
                       
                }
                
                model.addRow(v);
            }
        }catch(SQLException ex){
           JOptionPane.showMessageDialog(this,ex.getMessage());
           return;
        }
        tab_stock.setModel(model);
             int s=0;
           for(int j=0;j<tab_stock.getRowCount();j++){
              s+= Integer.parseInt(tab_stock.getValueAt(j,5).toString());
               res_stock.setText("Total: "+String.valueOf(s));
             
           }  
    }

